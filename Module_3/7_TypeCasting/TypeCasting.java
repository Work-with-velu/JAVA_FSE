public class TypeCasting 
{
    public static void main(String[] args) 
    {
        double d=123.15;
        int i=(int)d;
        System.out.println("Typecasted double Value is "+i);
        int j=27;
        double k=(double)j;
        System.out.println("Typecasted int Value is "+k);
    }
}
